let currentLang = localStorage.getItem('daycheck_lang') || 'ru';

export function t(key) {
  const lang = translations[currentLang];
  return (lang && lang[key]) || translations.ru[key] || key;
}

export function setLang(lang) {
  if (translations[lang]) {
    currentLang = lang;
    localStorage.setItem('daycheck_lang', lang);
  }
}

export function getLang() {
  return currentLang;
}

